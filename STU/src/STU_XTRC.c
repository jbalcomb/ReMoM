/*
TERRTYPE.LBX
5 * 512

*/
void Dump_TERRTYPE_LBX(void)
{
    int16_t * terrtype = 0;
    char terrtype_lbx_file[] = "TERRTYPE.LBX";

    terrtype = (int16_t *)Near_Allocate_First((5 * 512));

    LBX_Load_Data_Static(terrtype_lbx_file, 0, (SAMB_ptr)terrtype, 0, 5, 512);

    /*
        The first record is used for Ocean, Tundra, and Desert.
    */

    STU_File_Open(NULL, "TERRTYPE_DUMP.txt", "w");
    for(int i = 0; i < 5; i++)
    {
        fprintf(stdout, "Record %d:\n", i);
        for(int j = 0; j < 512; j++)
        {
            fprintf(stdout, "%d ", terrtype[i * 512 + j]);
        }
        fprintf(stdout, "\n");
    }

    /*
        The second record is used for Mountain and Hills,
        but, for Hills, it is offset 16 values into the record.
        My current theory is that Hills was mean to be the third record.
        That might also account for the oddity of there being 16 and only 16 values in the third record.
    */

}



/*
    Emit_Byte_Array
    Writes a uint8_t array as formatted hex to the output file.
    16 bytes per line, with trailing comma.
*/
void Emit_Byte_Array(FILE * out, const char * name, const uint8_t * data, uint32_t size)
{
    uint32_t i;

    if(name != NULL)
    {
        fprintf(out, "const uint8_t %s[] =\n{\n", name);
    }

    for(i = 0; i < size; i++)
    {
        if((i % 16) == 0)
        {
            fprintf(out, "    ");
        }
        fprintf(out, "0x%02X", data[i]);
        if(i < (size - 1))
        {
            fprintf(out, ",");
        }
        if(((i % 16) == 15) || (i == (size - 1)))
        {
            fprintf(out, "\n");
        }
        else
        {
            fprintf(out, " ");
        }
    }

    if(name != NULL)
    {
        fprintf(out, "};\n");
    }
}



/*
    Emit_U16_Array
    Writes a uint16_t array as formatted hex to the output file.
    8 values per line.
*/
static void Emit_U16_Array(FILE * out, const uint16_t * data, int count)
{
    int i;

    for(i = 0; i < count; i++)
    {
        if((i % 8) == 0)
        {
            fprintf(out, "        ");
        }
        fprintf(out, "0x%04X", data[i]);
        if(i < (count - 1))
        {
            fprintf(out, ",");
        }
        if(((i % 8) == 7) || (i == (count - 1)))
        {
            fprintf(out, "\n");
        }
        else
        {
            fprintf(out, " ");
        }
    }
}



