/*
    Extract_FONTS_LBX_0
    Loads FONTS.LBX entry [0] (font style data) and emits a
    struct s_FONTS_LBX_ENTRY_0 initialiser to the output file.
*/
void Extract_FONTS_LBX_0(FILE * out)
{
    SAMB_ptr data;
    struct s_FONT_HEADER * hdr;
    uint32_t entry_size;
    uint32_t glyph_size;

    data = LBX_Load("FONTS", 0);
    entry_size = lbxload_entry_length;
    hdr = (struct s_FONT_HEADER *)data;

    if(entry_size <= SZ_FONT_HDR)
    {
        fprintf(stderr, "Extract_FONTS_LBX_0: entry [0] too small (%u bytes)\n", entry_size);
        return;
    }

    glyph_size = entry_size - SZ_FONT_HDR;

    fprintf(out, "/*\n");
    fprintf(out, "    FONTS.LBX Entry [0] — Font Style Data\n");
    fprintf(out, "    Total size: %u bytes (header: %d, glyph data: %u)\n", entry_size, SZ_FONT_HDR, glyph_size);
    fprintf(out, "    Generated by Extract_FONTS_LBX_0()\n");
    fprintf(out, "*/\n");
    fprintf(out, "const struct s_FONTS_LBX_ENTRY_0 FONTS_LBX_ENTRY_0 =\n");
    fprintf(out, "{\n");
    fprintf(out, "    .header =\n");
    fprintf(out, "    {\n");

    fprintf(out, "        .current_colors =\n        {\n");
    Emit_U8_Array(out, hdr->current_colors, COLOR_SET_COUNT);
    fprintf(out, "        },\n");

    fprintf(out, "        .height = 0x%04X,\n", hdr->height);
    fprintf(out, "        .shadow_flag = 0x%02X,\n", hdr->shadow_flag);
    fprintf(out, "        .current_color_set = 0x%02X,\n", hdr->current_color_set);

    fprintf(out, "        .normal_colors =\n        {\n");
    Emit_U8_Array(out, hdr->normal_colors, COLOR_SET_COUNT);
    fprintf(out, "        },\n");

    fprintf(out, "        .highlight_colors =\n        {\n");
    Emit_U8_Array(out, hdr->highlight_colors, COLOR_SET_COUNT);
    fprintf(out, "        },\n");

    fprintf(out, "        .special_colors =\n        {\n");
    Emit_U8_Array(out, hdr->special_colors, COLOR_SET_COUNT);
    fprintf(out, "        },\n");

    fprintf(out, "        .current_base_height = 0x%04X,\n", hdr->current_base_height);
    fprintf(out, "        .current_vertical_spacing = 0x%04X,\n", hdr->current_vertical_spacing);
    fprintf(out, "        .current_horizontal_spacing = 0x%04X,\n", hdr->current_horizontal_spacing);

    fprintf(out, "        .current_font_widths =\n        {\n");
    Emit_U8_Array(out, hdr->current_font_widths, 96);
    fprintf(out, "        },\n");

    fprintf(out, "        .current_data_offsets =\n        {\n");
    Emit_U16_Array(out, hdr->current_data_offsets, 96);
    fprintf(out, "        },\n");

    fprintf(out, "        .base_height =\n        {\n");
    Emit_U16_Array(out, hdr->base_height, 8);
    fprintf(out, "        },\n");

    fprintf(out, "        .horizontal_spacing =\n        {\n");
    Emit_U16_Array(out, hdr->horizontal_spacing, 8);
    fprintf(out, "        },\n");

    fprintf(out, "        .vertical_spacing =\n        {\n");
    Emit_U16_Array(out, hdr->vertical_spacing, 8);
    fprintf(out, "        },\n");

    fprintf(out, "        .font_widths =\n        {\n");
    Emit_U8_Array(out, hdr->font_widths, 768);
    fprintf(out, "        },\n");

    fprintf(out, "        .data_offsets =\n        {\n");
    Emit_U16_Array(out, hdr->data_offsets, 768);
    fprintf(out, "        }\n");

    fprintf(out, "    },\n");

    fprintf(out, "    .glyph_data =\n    {\n");
    Emit_Byte_Array(out, NULL, data + SZ_FONT_HDR, glyph_size);
    fprintf(out, "    }\n");

    fprintf(out, "};\n");
    fprintf(out, "const uint32_t FONTS_LBX_ENTRY_0_SIZE = %u;\n\n\n", entry_size);
}



/*
    Extract_FONTS_LBX_1
    Loads FONTS.LBX entry [1] (border/outline data) and emits
    a raw byte array to the output file.
*/
void Extract_FONTS_LBX_1(FILE * out)
{
    SAMB_ptr data;
    uint32_t entry_size;

    data = LBX_Load("FONTS", 1);
    entry_size = lbxload_entry_length;

    fprintf(out, "/*\n");
    fprintf(out, "    FONTS.LBX Entry [1] — Border / Outline Data\n");
    fprintf(out, "    Total size: %u bytes\n", entry_size);
    fprintf(out, "    Generated by Extract_FONTS_LBX_1()\n");
    fprintf(out, "*/\n");

    Emit_Byte_Array(out, "FONTS_LBX_ENTRY_1", data, entry_size);
    fprintf(out, "const uint32_t FONTS_LBX_ENTRY_1_SIZE = %u;\n\n\n", entry_size);
}



/*
    Extract_FONTS_LBX_2
    Loads FONTS.LBX entry [2] (palette block) and emits a
    struct s_FONTS_LBX_PALETTE initialiser to the output file.
*/
void Extract_FONTS_LBX_2(FILE * out)
{
    SAMB_ptr palette_buf;
    SAMB_ptr pal_data;
    uint32_t entry_size;

    palette_buf = Allocate_Space(348);
    pal_data = LBX_Reload("FONTS", 2, palette_buf);
    entry_size = lbxload_entry_length;

    fprintf(out, "/*\n");
    fprintf(out, "    FONTS.LBX Entry [2] — Palette Block\n");
    fprintf(out, "    Total size: %u bytes\n", entry_size);
    fprintf(out, "    Generated by Extract_FONTS_LBX_2()\n");
    fprintf(out, "*/\n");
    fprintf(out, "const struct s_FONTS_LBX_PALETTE FONTS_LBX_ENTRY_2 =\n");
    fprintf(out, "{\n");

    fprintf(out, "    .rgb_palette =\n    {\n");
    Emit_Byte_Array(out, NULL, pal_data + 0x000, 768);
    fprintf(out, "    },\n");

    fprintf(out, "    .font_colors =\n    {\n");
    Emit_Byte_Array(out, NULL, pal_data + 0x300, 256);
    fprintf(out, "    },\n");

    fprintf(out, "    .palette_data_0x400 =\n    {\n");
    Emit_Byte_Array(out, NULL, pal_data + 0x400, 256);
    fprintf(out, "    },\n");

    fprintf(out, "    .mouse_palette =\n    {\n");
    Emit_Byte_Array(out, NULL, pal_data + 0x500, 4096);
    fprintf(out, "    },\n");

    fprintf(out, "    .remap_colors =\n    {\n");
    Emit_Byte_Array(out, NULL, pal_data + 0x1500, 96);
    fprintf(out, "    }\n");

    fprintf(out, "};\n");
    fprintf(out, "const uint32_t FONTS_LBX_ENTRY_2_SIZE = %u;\n\n", entry_size);
}



/*
    Extract_FONTS_LBX
    Top-level function: creates the output file and calls all three entry extractors.
*/
void Extract_FONTS_LBX(const char * out_path)
{
    FILE * out;

    out = stu_fopen(out_path, "w");
    if(out == NULL)
    {
        fprintf(stderr, "Extract_FONTS_LBX: cannot open %s for writing\n", out_path);
        return;
    }

    fprintf(out, "/*\n");
    fprintf(out, "    FONTS_LBX.c — Embedded FONTS.LBX data\n");
    fprintf(out, "    Generated by Extract_FONTS_LBX() — do not edit by hand.\n");
    fprintf(out, "    Regenerate from original FONTS.LBX using the extraction tool.\n");
    fprintf(out, "*/\n\n");
    fprintf(out, "#include \"FONTS_LBX.h\"\n\n\n");

    Extract_FONTS_LBX_0(out);
    Extract_FONTS_LBX_1(out);
    Extract_FONTS_LBX_2(out);

    fclose(out);
    fprintf(stdout, "Extract_FONTS_LBX: wrote %s\n", out_path);
}
