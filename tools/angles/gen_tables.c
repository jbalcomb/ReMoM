/*
    gen_tables.c - Trigonometric Lookup Table Generator

    Generates the pre-computed fixed-point trigonometric lookup tables
    used by Master of Magic (WIZARDS.EXE seg034).

    In the DOS era, developers used tools like this to pre-compute
    tables on their development workstations, avoiding slow floating-point
    math at runtime on 386/486 processors.

    Fixed-point formats:
        Tangent:   8.8 fixed-point (scale factor 256)
        Sin/Cos:   0.16 fixed-point (scale factor 65536)

    Build:
        gcc -Wall -O2 -o gen_tables gen_tables.c -lm

    Usage:
        gen_tables           Verify generated tables against WIZARDS.EXE data
        gen_tables --emit    Output tables as C source code
*/

