static void emit_tables(
    const unsigned char  *tan_0_44,
    const unsigned short *tan_45_89,
    const unsigned short *sin_tbl,
    const unsigned short *cos_tbl)
{
    int i;

    printf("/* Generated by tools/angles/gen_tables.c */\n\n");

    /* Tangents_0_44 */
    printf("/* floor(tan(deg) * 256) for deg = 0..44 */\n");
    printf("static const uint8_t Tangents_0_44[45] = {\n");
    for (i = 0; i < 45; i++)
    {
        if (i % 9 == 0) printf("    ");
        printf("0x%02X", tan_0_44[i]);
        if (i < 44) printf(",");
        if ((i + 1) % 9 == 0 || i == 44) printf("\n");
    }
    printf("};\n\n");

    /* Tangents_45_89 */
    printf("/* floor(tan(deg) * 256) for deg = 45..89 */\n");
    printf("static const uint16_t Tangents_45_89[45] = {\n");
    for (i = 0; i < 45; i++)
    {
        if (i % 8 == 0) printf("    ");
        printf("0x%04X", tan_45_89[i]);
        if (i < 44) printf(",");
        if ((i + 1) % 8 == 0 || i == 44) printf("\n");
    }
    printf("};\n\n");

    /* angle_to_sin */
    printf("/* floor(sin(deg) * 65536) for deg = 0..89 */\n");
    printf("static const uint16_t angle_to_sin[90] = {\n");
    for (i = 0; i < 90; i++)
    {
        if (i % 8 == 0) printf("    ");
        printf("0x%04X", sin_tbl[i]);
        if (i < 89) printf(",");
        if ((i + 1) % 8 == 0 || i == 89) printf("\n");
    }
    printf("};\n\n");

    /* angle_to_cos */
    printf("/* floor(cos(deg) * 65536) for deg = 1..90 */\n");
    printf("static const uint16_t angle_to_cos[90] = {\n");
    for (i = 0; i < 90; i++)
    {
        if (i % 8 == 0) printf("    ");
        printf("0x%04X", cos_tbl[i]);
        if (i < 89) printf(",");
        if ((i + 1) % 8 == 0 || i == 89) printf("\n");
    }
    printf("};\n");
}


static int verify_table_u8(
    const char *name,
    const unsigned char *generated,
    const unsigned char *expected,
    int count,
    int deg_offset)
{
    int i, mm = 0;

    printf("%s  [%d entries, uint8]\n", name, count);
    for (i = 0; i < count; i++)
    {
        if (generated[i] != expected[i])
        {
            printf("  [%2d] %2d deg: expected %3u (0x%02X), got %3u (0x%02X), diff %+d\n",
                i, i + deg_offset,
                expected[i], expected[i],
                generated[i], generated[i],
                (int)generated[i] - (int)expected[i]);
            mm++;
        }
    }
    printf("  Result: %s (%d/%d match)\n\n",
        mm == 0 ? "PASS" : "FAIL", count - mm, count);
    return mm;
}


static int verify_table_u16(
    const char *name,
    const unsigned short *generated,
    const unsigned short *expected,
    int count,
    int deg_offset)
{
    int i, mm = 0;

    printf("%s  [%d entries, uint16]\n", name, count);
    for (i = 0; i < count; i++)
    {
        if (generated[i] != expected[i])
        {
            printf("  [%2d] %2d deg: expected %5u (0x%04X), got %5u (0x%04X), diff %+d\n",
                i, i + deg_offset,
                expected[i], expected[i],
                generated[i], generated[i],
                (int)generated[i] - (int)expected[i]);
            mm++;
        }
    }
    printf("  Result: %s (%d/%d match)\n\n",
        mm == 0 ? "PASS" : "FAIL", count - mm, count);
    return mm;
}


int main(int argc, char *argv[])
{
    int emit_mode = (argc > 1 && strcmp(argv[1], "--emit") == 0);
    unsigned char  gen_tan_0_44[45];
    unsigned short gen_tan_45_89[45];
    unsigned short gen_sin[90];
    unsigned short gen_cos[90];
    int i;
    double rad, val;

    /*
        Generate Tangents_0_44
        floor(tan(i deg) * 256) for i = 0..44
        Scale factor 256 = 8.8 fixed-point
        tan(44) * 256 = 247, fits in uint8
    */
    for (i = 0; i <= 44; i++)
    {
        rad = i * (PI / 180.0);
        val = tan(rad) * 256.0;
        gen_tan_0_44[i] = (unsigned char)val;
    }

    /*
        Generate Tangents_45_89
        floor(tan(i deg) * 256) for i = 45..89
        tan(45) * 256 = 256, needs uint16
        tan(89) * 256 = ~14666, fits in uint16
    */
    for (i = 45; i <= 89; i++)
    {
        rad = i * (PI / 180.0);
        val = tan(rad) * 256.0;
        gen_tan_45_89[i - 45] = (unsigned short)val;
    }

    /*
        Generate Sin table
        floor(sin(i deg) * 65536) for i = 0..89
        Scale factor 65536 = 0.16 fixed-point
        sin(89) * 65536 = 65526, fits in uint16
    */
    for (i = 0; i <= 89; i++)
    {
        rad = i * (PI / 180.0);
        val = sin(rad) * 65536.0;
        gen_sin[i] = (unsigned short)val;
    }

    /*
        Generate Cos table
        floor(cos(i deg) * 65536) for i = 1..90
        cos(1) * 65536 = 65526, cos(90) * 65536 = 0
    */
    for (i = 1; i <= 90; i++)
    {
        rad = i * (PI / 180.0);
        val = cos(rad) * 65536.0;
        gen_cos[i - 1] = (unsigned short)val;
    }


    if (emit_mode)
    {
        emit_tables(gen_tan_0_44, gen_tan_45_89, gen_sin, gen_cos);
        return 0;
    }


    /* --- Verify mode --- */
    int total_mismatches = 0;

    printf("Angle Lookup Table Verification\n");
    printf("================================\n");
    printf("Comparing generated tables against WIZARDS.EXE seg034 data\n\n");

    total_mismatches += verify_table_u8(
        "Tangents_0_44 ", gen_tan_0_44, expected_tan_0_44, 45, 0);

    total_mismatches += verify_table_u16(
        "Tangents_45_89", gen_tan_45_89, expected_tan_45_89, 45, 45);

    total_mismatches += verify_table_u16(
        "angle_to_sin  ", gen_sin, expected_sin, 90, 0);

    total_mismatches += verify_table_u16(
        "angle_to_cos  ", gen_cos, expected_cos, 90, 1);

    printf("================================\n");
    printf("Total: %d / 270 entries match\n", 270 - total_mismatches);

    if (total_mismatches == 0)
    {
        printf("VERIFIED: All tables match WIZARDS.EXE seg034 data exactly.\n");
    }
    else
    {
        printf("%d entries differ.\n", total_mismatches);
        printf("Differences of +/-1 are expected due to Borland C++ 3.0\n");
        printf("x87 FPU precision vs modern IEEE 754 double precision.\n");
    }

    return total_mismatches > 0 ? 1 : 0;
}
